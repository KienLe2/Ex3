public class Loop1to100 {
    public static void main(String[] args) {
        int n = 100; // 100 num
        for (int i = 0; i <= n; i++) { //loop
            if (i % 2 == 0) { // device two 2 if == 0 if true
                System.out.println("Sums even of numbers: " + i);
            }
        }
    }
}
